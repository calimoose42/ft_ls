#include "libft.h"

void	ft_print_table(char **tab)
{
	int i;
	int j;

	j = 0;
	while (tab[j] != 0)
	{
		i = 0;
		while (tab[j][i])
			ft_putchar(tab[j][i++]);
		write(1, "\n", 1);
		j++;
	}
}
